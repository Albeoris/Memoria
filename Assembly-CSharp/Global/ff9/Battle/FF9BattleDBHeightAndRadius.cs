    public static Boolean TryFindHeightAndRadius(Int32 geoId, ref Int32 height, ref Int32 radius)
    {
        Int32 index;
        if (BinarySearch(geoId, out index))
        {
            height = Data[index, 1];
            radius = Data[index, 2];
            return true;
        }

        return false;
    }

    public static Boolean TryFindNeckBoneIndex(Int32 geoId, ref Int32 boneIndex)
    {
        Int32 index;
        if (BinarySearch(geoId, out index))
        {
            boneIndex = Data[index, 3];
            return true;
        }

        return false;
    }

    private static Boolean BinarySearch(Int32 geoId, out Int32 index)
    {
        Int32 first = 0;
        Int32 last = GeoCount;

        if (Data[0, 0] > geoId)
        {
            index = -1;
            return false;
        }

        if (Data[GeoCount - 1, 0] < geoId)
        {
            index = -1;
            return false;
        }

        while (first < last)
        {
            Int32 mid = first + ((last - first) >> 1);

            Int32 value = Data[mid, 0];
            if (value == geoId)
            {
                index = mid;
                return true;
            }

            if (geoId < value)
                last = mid;
            else
                first = mid + 1;
        }

        index = -1;
        return false;
    }

