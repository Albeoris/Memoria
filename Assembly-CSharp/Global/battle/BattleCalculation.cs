using System;

public class BattleCalculation
{
    public BattleCalculation()
    {
        this.atkStart = 0;
        this.atkFinish = 0;
        this.defStart = 0;
        this.defFinish = 0;
    }

    public Int32 atkStart;

    public Int32 atkFinish;

    public Int32 defStart;

    public Int32 defFinish;
}
